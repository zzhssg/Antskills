def fallback_decision(l2_output: dict, user_context: dict) -> dict:
    """Template-based fallback when LLM is unavailable."""
    products = l2_output.get("scored_products", [])
    stats = l2_output.get("market_stats", {})
    principal = user_context.get("principal_usd", 10000)
    
    if not products:
        return {
            "headline": "暂无符合条件的产品",
            "top1_reason": "当前筛选条件下没有找到合适的产品，建议放宽筛选条件后重试。",
            "opportunity_cost": "",
            "risk_warning": "以上分析基于历史数据和公开信息，不构成投资建议。请自行评估风险。",
            "anomaly_narratives": [],
            "brief_for_card": [],
        }
    
    top1 = products[0]
    annual = principal * top1.get("apy", 0)
    median_annual = principal * stats.get("median_apy", 0)
    
    return {
        "headline": f"综合评分最高: {top1['platform']} {top1['product_name']}",
        "top1_reason": (
            f"{top1['platform']} 的 {top1['product_name']} "
            f"在 {len(products)} 个产品中综合评分第一 "
            f"(评分 {top1.get('total_score', 0):.0f}/100)，"
            f"年化收益率 {top1.get('apy', 0)*100:.1f}%。"
        ),
        "opportunity_cost": (
            f"按 ${principal:,.0f} 本金计算，"
            f"选择 TOP 1 年收益约 ${annual:,.0f}，"
            f"比市场中位数多 ${annual - median_annual:,.0f}。"
        ),
        "risk_warning": "以上分析基于历史数据和公开信息，不构成投资建议。收益率可能随市场变化，请自行评估风险。",
        "anomaly_narratives": [],
        "brief_for_card": [
            {"rank": i + 1, "one_liner": f"综合评分 {p.get('total_score', 0):.0f}/100，APY {p.get('apy', 0)*100:.1f}%"}
            for i, p in enumerate(products[:3])
        ],
    }


# ─── Entry Point ───────────────────────────────────────

def run(l2_output: dict, user_context: dict) -> dict:
    """Run L3: try LLM, fall back to template."""
    try:
        return call_llm(l2_output, user_context)
    except Exception as e:
        print(f"[L3] LLM failed ({e}), using fallback")
        return fallback_decision(l2_output, user_context)
