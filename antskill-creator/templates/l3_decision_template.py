import json
import os

# ─── Config ────────────────────────────────────────────

MODEL = "claude-sonnet-4-20250514"
MAX_TOKENS = 1000
TEMPERATURE = 0.3

SYSTEM_PROMPT = """
# Role
你是{{角色名}}，专注于{{领域}}。

# Input
你会收到一个 JSON 对象，包含: user_context, market_stats, scored_products, anomaly_alerts。

# Output
严格返回以下 JSON，不要包含任何 Markdown 或额外文字：

{
  "headline": "不超过 40 字的核心结论",
  "top1_reason": "80-120 字，解释为什么 TOP 1 最优",
  "opportunity_cost": "包含金额对比的机会成本分析",
  "risk_warning": "至少 40 字的风险提示",
  "anomaly_narratives": ["异常 1 的人话解释"],
  "brief_for_card": [
    {"rank": 1, "one_liner": "一句话点评"}
  ]
}

# Writing Rules
1. 首句即结论 — headline 第一个词必须是平台名或关键数字
2. 金字塔结构 — 结论 > 论据 > 细节
3. 风险对称 — 推荐了什么就必须在 risk_warning 中提及对应风险
4. 机会成本 — 必须包含"如果选 X 而不选 Y，每年差 $Z"的对比
5. 异常叙事 — 把 anomaly_alerts 翻译成人话，含具体数字
6. 禁止 — 不含"我认为"、"我觉得"、"我建议"
""".strip()


# ─── LLM Call ──────────────────────────────────────────

def call_llm(l2_output: dict, user_context: dict) -> dict:
    """
    Call Anthropic API. Returns parsed JSON or raises.
    In production, this should go through a backend proxy.
    """
    import urllib.request
    
    api_key = os.environ.get("ANTHROPIC_API_KEY", "")
    if not api_key:
        raise EnvironmentError("ANTHROPIC_API_KEY not set")
    
    payload = {
        "model": MODEL,
        "max_tokens": MAX_TOKENS,
        "temperature": TEMPERATURE,
        "system": SYSTEM_PROMPT,
        "messages": [
            {
                "role": "user",
                "content": json.dumps({
                    "user_context": user_context,
                    "market_stats": l2_output["market_stats"],
                    "scored_products": l2_output["scored_products"][:5],
                    "anomaly_alerts": l2_output["anomaly_alerts"],
                }, ensure_ascii=False)
            }
        ]
    }
    
    req = urllib.request.Request(
        "https://api.anthropic.com/v1/messages",
        data=json.dumps(payload).encode(),
        headers={
            "Content-Type": "application/json",
            "x-api-key": api_key,
            "anthropic-version": "2023-06-01",
        }
    )
    
    with urllib.request.urlopen(req, timeout=30) as resp:
        data = json.loads(resp.read())
    
    # Extract text from response
    text = ""
    for block in data.get("content", []):
        if block.get("type") == "text":
            text += block["text"]
    
    # Clean and parse
    text = text.strip()
    if text.startswith("```"):
        text = text.split("\n", 1)[1] if "\n" in text else text[3:]
    if text.endswith("```"):
        text = text[:-3]
    text = text.strip()
    
    return json.loads(text)


